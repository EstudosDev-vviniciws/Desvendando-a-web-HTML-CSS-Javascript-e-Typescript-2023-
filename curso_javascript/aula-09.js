
function soma(item1, item2){
return item1 + item2;
}

function diminuir(item1, item2){
    return item1 - item2;
}

console.log('Soma:', soma(5, 6));
console.log('Soma', soma(550, 1200));
console.log('Soma:', diminuir (10, 6));


